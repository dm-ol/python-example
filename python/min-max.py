# Простий приклад роботи min та max
# Задаємо список чисел
list_1 = [2, 42, 13, 88, 36, 49, 114, 99, 41, 9, 63, 200]
# Перевір, яке з чисел найбільше
# і передайте його largest_number variable.
 
largest_number = max(list_1)
smallest_number = min(list_1)
 
# Виведіть результат
print("Найбільше число це:", largest_number)
print("Найменше число це:", smallest_number)