# Передача іменованого аргументу. Python пропонує іншу конструкцію передачі аргументів,
# коли значення аргументу визначається його іменем, а не позицією
def introduction(first_name, last_name):
    print("Привіт, мене звати", first_name, last_name)
 
introduction(first_name = "Джеймс", last_name = "Бонд")
introduction(last_name = "Скайвокер", first_name = "Люк")
 
# Змішування позиційних і іменованих аргументів. Якщо хочете, то ви можете змішати обидва варіанти,
# але є одне непорушне правило: позиційні аргументи потрібно ставити перед аргументами ключових слів.
def adding(a, b, c):
    print(a, "+", b, "+", c, "=", a + b + c)
 
adding(3, c = 1, b = 2) # a = позиційна передача, c,d = іменована

# значення за замовчуванням (наперед визначені), якщо їх відповідні аргументи були відсутні.
def introduction(first_name, last_name="Сміт"):
     print("Привіт, мене звати", first_name, last_name)
 
introduction("Генрі") # виведе: Генрі Сміт
introduction(first_name="Вільям") # виведе: Вільям Сміт
introduction("Бетті", "Джонсон") # виведе: Бетті Джонсон (ключове слово аргументу замінено на "Джонсон")

 
