# Позиційна передача параметрів. Прийом, при якому і-му (першому, другому і т.д.) параметру
# функції# присвоюється і-й (перший, другий і т.д.) аргумент, називається позиційною передачею
# параметрів, а аргументи, що передаються таким чином, називаються позиційними аргументами.
def introduction(first_name, last_name):
    print("Привіт, мене звати", first_name, last_name)
 
introduction("Люк", "Скайвокер")
introduction("Джессі", "Уілсон")
introduction("Кларк", "Кент")

