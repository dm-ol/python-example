# Функція dir() повертає список допустимих атрибутів переданого об’єкту.
print("dir для integer")
number = int(12)
print(dir(number))

print("dir для float")
number = int(12)
print(dir(number))

print("dir для списку")
list_1 = [11, 12, 13, 14]
print(dir(list_1))

print("dir для кортежу")
tuple_list = (1, 12, 42, 55)
print(dir(tuple_list))