# Функція oct() конвертує ціле число у вісімкове представлення.

x = oct(12)

print(x)

# Конвертація з десяткової системи числення у вісімкову
print('oct(10) is:', oct(10))

# Конвертація з двійкової системи числення у вісімкову
print('oct(0b101) is:', oct(0b101))

# Конвертація з шістнадцяткової системи числення у вісімкову
print('oct(0XA) is:', oct(0XA))
