# Розпаковка та об'єднання словників за допомогою оператора **

car = {
    "brand": "Ford",
    "model": "Mustang",
    "year": 1964
}

car_price = {
    **car,  # Беремо словник cars та доповнюємо його новим ключем
    "price": 120000
}

print(car_price)  # Новий словник з ціною

print(car)  # Перший словник без змін
