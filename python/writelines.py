# Метод writelines() записує елементи списку в файл.
# Місце вставки тексту залежить від режиму файлу та позиції потоку.

f = open("test.txt", "a")
f.writelines(["See you soon!", "Over and out."])
f.close()

f = open("test.txt", "r")
print(f.read())

# У режимі "a" тексти будуть вставлені в поточну позицію файлового потоку
# за замовчуванням в кінець файлу. У режимі "w" файл буде очищено до того,
# як тексти будуть вставлені в поточну позицію файлового потоку, за замовчуванням 0.
