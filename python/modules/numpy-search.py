# Можна проводити пошук у масиві для певного значення та повертати індекси,
# де відбувається збіг. Для цього використовуйте метод where().

import numpy as np

arr = np.array([1, 2, 3, 4, 5, 4, 4])

x = np. where(arr == 4)

print(x)
# (array([3, 5, 6],)

# У цьому прикладі повернеться кортеж: (array([3, 5, 6],).
# Це означає, що значення 4 є в індексах 3, 5 і 6.
