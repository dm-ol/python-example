# Простий, але зручний та корисний пакет для створення QR-кодів.
# Для малювання картинок використовується Pillow, тому переконайтеся, що він у вас встановлений.

# Щоб створити об'єкт картинки з QR-кодом, треба викликати метод make()
# і передати дані як аргумент. Для збереження застосовуємо метод save() до отриманого об'єкту.

# pip install qrcode[pil]
import qrcode
image = qrcode.make('https://example.com')
image.save('qr.png')
