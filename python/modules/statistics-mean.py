# Метод statistics.mean() обчислює середнє арифметичне значення заданого набору даних.
# Він складає всі задані значення, після чого ділить на їх кількість.

import statistics

print(statistics.mean([1, 3, 5, 7, 9, 11, 13]))
# 7
print(statistics.mean([1, 3, 5, 7, 9, 11]))
# 6
print(statistics.mean([-11, 5.5, -3.4, 7.1, -9, 22]))
# 1.8666666666666667

# Якщо дані не вказані, повертається помилка StatisticsError.
