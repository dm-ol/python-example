# Існує кілька бібліотек на Python для захоплення екрана комп'ютера під керуванням ОС Windows,
# але найшвидшою виявилася бібліотека d3dshot (https://github.com/SerpentAI/D3DShot).
# Вона використовує системні бібліотеки DXGI та Direct3D,
# щоб забезпечити надзвичайно швидку та надійну функціональність захоплення екрану.

import d3dshot

# Ініціалізація
d = d3dshot.create()

# Отримати скріншот у змінну
# з типом PIL.Image
img = d.screenshot()
