# Метод math.fsum() повертає суму всіх елементів у будь-якій ітерації. Ітерацією може бути кортеж, масив, список.

import math

print(math.fsum([1, 2, 3, 4, 5]))
# 15.0
print(math.fsum([100, 400, 340, 500]))
# 1340.0
print(math.fsum([1.7, 0.3, 1.5, 4.5]))
# 8.0

# Він повертає суму всіх значень як значення з плаваючою комою. Якщо об'єкт, що ітерується, не є числом — TypeError.
