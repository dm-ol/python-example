# Ваша задача написати програму, яка зчитує кількість блоків, наявних у будівельників,
# і обчислює висоту піраміди, яку можна побудувати з цих блоків.
# Примітка: висота вимірюється кількістю повністю завершених шарів. Якщо будівельники не мають
# достатньої кількості блоків і не можуть завершити наступний шар, вони негайно закінчують роботу.
# Піраміда складається за одним простим принципом: кожен нижній шар містить на один блок більше, ніж верхній.
blocks = int(input("Введіть кількість блоків: "))

# Ініціалізуємо змінну для збереження висоти піраміди
height = 0
# Ініціалізуємо змінну для збереження кількості блоків, необхідних для наступного рівня
blocks_needed = 1

# Поки у нас є блоки, спробуйте будувати нові рівні піраміди
while blocks >= blocks_needed:
    height = height + 1  # Збільшуємо висоту піраміди
    # Віднімаємо блоки, що були використані для поточного рівня
    blocks = blocks - blocks_needed
    # Збільшуємо кількість блоків, необхідних для наступного рівня
    blocks_needed = blocks_needed + 1

print("Висота піраміди:", height)
